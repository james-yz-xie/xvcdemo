import type { Chapter } from "../types";

// ─────────────────────────────────────────────────────────────
// 5W1H summary prompt builder
// ─────────────────────────────────────────────────────────────

export function buildSummaryPrompt(
  fullArticle: string,
  chapter: Chapter
): string {
  return [
    "你是一位专业编辑。请基于以下视频完整文章和当前章节，提炼出该章节的 5W1H 总结。",
    "",
    "## 完整文章内容",
    fullArticle,
    "",
    "## 当前章节",
    `标题：${chapter.title}`,
    `内容：${chapter.content}`,
    "",
    "## 输出要求",
    "请以纯 JSON 格式输出，不要包含 markdown 代码块标记，不要添加任何额外文字：",
    "",
    '{',
    '  "who": "涉及的人物、组织或主体（一句话）",',
    '  "what": "该章节讨论的核心内容或事件（一句话）",',
    '  "when": "相关的时间点或时间段（一句话）",',
    '  "where": "涉及的地点、市场或领域（一句话）",',
    '  "why": "为什么会这样，背后的原因或驱动力（一句话）",',
    '  "how": "如何实现、运作或达成的（一句话）"',
    '}',
    "",
    "要求每个字段简洁凝练，一句话概括，不超过 80 字。",
  ].join("\n");
}
